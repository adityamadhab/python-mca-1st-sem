# Program to show use of comparison operators in Python

x = 10
y = 20

print("x =", x)
print("y =", y)

# Equal to
print("x == y:", x == y)

# Not equal to
print("x != y:", x != y)

# Greater than
print("x > y:", x > y)

# Less than
print("x < y:", x < y)

# Greater than or equal to
print("x >= y:", x >= y)

# Less than or equal to
print("x <= y:", x <= y)
