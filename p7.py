# Program to show use of bitwise operators in Python

a = 10  # 1010 in binary
b = 4   # 0100 in binary

print("a =", a)
print("b =", b)

# Bitwise AND
print("a & b:", a & b)

# Bitwise OR
print("a | b:", a | b)

# Bitwise XOR
print("a ^ b:", a ^ b)

# Bitwise NOT
print("~a:", ~a)

# Bitwise left shift
print("a << 1:", a << 1)

# Bitwise right shift
print("a >> 1:", a >> 1)
