# Program to show different datatypes in Python(Numeric and Text)

# Numeric datatypes
integer_num = 10
float_num = 3.14
complex_num = 2 + 3j

print("Integer:", integer_num, type(integer_num))
print("Float:", float_num, type(float_num))
print("Complex:", complex_num, type(complex_num))

# Text datatype
text_str = "Hello, World!"
print("String:", text_str, type(text_str))
