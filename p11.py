# Program to show string slicing in Python

s = "Hello, World!"

print("Original string:", s)
print("s[0:5]:", s[0:5])
print("s[7:12]:", s[7:12])
print("s[:5]:", s[:5])
print("s[7:]:", s[7:])
print("s[-6:-1]:", s[-6:-1])
print("s[::-1]:", s[::-1])
print("s[::2]:", s[::2])
print("s[1:10:2]:", s[1:10:2])
