# Program to show use of logical operators in Python

a = True
b = False

print("a =", a)
print("b =", b)

# Logical AND
print("a and b:", a and b)

# Logical OR
print("a or b:", a or b)

# Logical NOT
print("not a:", not a)
print("not b:", not b)

# Logical operators with expressions
x = 5
y = 10
print("(x > 0) and (y < 20):", (x > 0) and (y < 20))
print("(x == 5) or (y == 5):", (x == 5) or (y == 5))
print("not (x < y):", not (x < y))
